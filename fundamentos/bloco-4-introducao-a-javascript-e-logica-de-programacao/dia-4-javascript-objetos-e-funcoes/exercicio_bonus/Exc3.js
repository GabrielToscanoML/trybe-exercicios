function retorna_quantidade(basket){

let cesta = {
  Melancia: 0,
  Abacate: 0,
  Uva: 0,
  Laranja: 0,
  Jaca: 0,
  Pera: 0,
  Banana: 0,
}

for (let index = 0; index<basket.length; index++) {
  comparaPalavra(basket[index], cesta);
}
return cesta;
}

function comparaPalavra(palavra, cesta){
  for(let key in cesta){
    if(palavra === key){
      cesta[key]++;
    }
  }
}